		      <p> HTML (HyperTet Markup Language) est un langage descriptif utilisé pour structurer le contenu d'une page (ses textes, ses images, ses liens...) <br/> Un document HTML est un fichier texte qui contient des balises (ou tag), ces balises doivent être utilisées d'une certaine façon pour décrire correctement la structure d'un document. Les balises indique au navigateur comment afficher le document. 
		      </p>

